package Oracle;

public class exercicio2parte2 {
    public static void main(String[] args) {
        //usar no minimo 20 caracteres especiais para fazer arte ASCII
        //resolvi modificar a carinha ja feita na primeira parte do exercicio, mas usando outros caracteres.
        System.out.println("  ....  ");
        System.out.println(" .    .");
        System.out.println(". ~  ~ .");
        System.out.println(". o  o .");
        System.out.println(".      .");
        System.out.println(". //// .");
        System.out.println(". *  * .");
        System.out.println(".  **  .");
        System.out.println(". |||| .");
        System.out.println(" .    . ");
        System.out.println("  ....  ");
        //Explicando os caracteres e o que cada um representa: os ''.'' são o contorno do rosto; os '~' as sobrancelhas,
        //a letra 'o' representa os olhos, as / é o bigode, os '*' fazem o sorriso feliz do homem, e as '|' representa a barba do hominho.
    }
    
}
